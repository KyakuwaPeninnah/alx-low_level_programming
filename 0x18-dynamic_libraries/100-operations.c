#include <stdio.h>
#include "main.h"
/**
 * add - adds two numbers
 * sub - subtracts two numbers
 * mul - multiplies two integers
 * div - divides two intergers
 * mod - divides two numbers and return its remainder
 * @a: int type num
 * @b: int type num
 * Return: result of multiplication
 */

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int mul(int a, int b)
{
	return (a * b);
}

int div(int a, int b)
{
	return (a / b);
}

int mod(int a, int b)
{
	return (a % b);
}
